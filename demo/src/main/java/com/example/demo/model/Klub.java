package com.example.demo.model;

public class Klub {
	
	
	private int id_kluba;
	private String ime_kluba;
	private String mjesto_kluba;
	private int id_dvorana;
	
	
	public int getId_kluba() {
		return id_kluba;
	}
	public void setId_kluba(int id_kluba) {
		this.id_kluba = id_kluba;
	}
	public String getIme_kluba() {
		return ime_kluba;
	}
	public void setIme_kluba(String ime_kluba) {
		this.ime_kluba = ime_kluba;
	}
	public String getMjesto_kluba() {
		return mjesto_kluba;
	}
	public void setMjesto_kluba(String mjesto_kluba) {
		this.mjesto_kluba = mjesto_kluba;
	}
	public int getId_dvorana() {
		return id_dvorana;
	}
	public void setId_dvorana(int id_dvorana) {
		this.id_dvorana = id_dvorana;
	}

}
